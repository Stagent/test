package module_info;
public class Dieuthintis {
    
    private final String username;
    private final String password;
    
    public Dieuthintis(String username, String password)
            
    {
    	
        this.username = username;
        
        this.password = password;
        
    }
    
    String get_username()
            
    {
        
     return username;   
    }
    
    String get_password()
            
    {
     return password;   
    }
    }
    